package com.chandrakant;

public class ForLoopClass {
    public static void main(String[] args) {
        // for Loop
        System.out.println("----------------------------------");
        for (int i = 0; i < 8; i++) {
            System.out.println("C==>"+i);
        }
        System.out.println("for loop completed");
        System.out.println("----------------------------------");
    }
}

