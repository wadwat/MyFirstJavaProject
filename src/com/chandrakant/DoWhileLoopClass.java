package com.chandrakant;

public class DoWhileLoopClass {
    public static void main(String[] args) {
        //do-while Loop
        System.out.println("----------------------------------");
        int x = 0;
        do {
            System.out.println("C=>"+x);
            x++;
        } while (x <= 10);
        System.out.println("do-while loop completed");
        System.out.println("----------------------------------");
    }
}
